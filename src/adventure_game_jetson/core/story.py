PLAIN_LOOPS = {12, 13, 24, 25, 26, 27}
NO_EVENT_AFTER = {12, 24, 25, 26}


@dataclass(slots=True)
class StoryTeller:
    api_key: str = ""
    client: object | None = field(init=False, default=None)
    cache: dict[int, str] = field(init=False, default_factory=dict)

    def __post_init__(self) -> None:
        if OpenAI and self.api_key:
            try:
                self.client = OpenAI(api_key=self.api_key)
            except Exception:
                self.client = None

    def get_prompt(self, loop_id: int) -> str:
        if loop_id <= 5:
            return LLM_PROMPT_CH1
        if loop_id <= 12:
            return LLM_PROMPT_CH2
        return LLM_PROMPT_CH3

    def generate_text(self, loop_id: int) -> str:
        if loop_id in self.cache:
            return self.cache[loop_id]
        raw = GAME_LOOPS.get(loop_id, "")
        if loop_id in PLAIN_LOOPS or not self.client:
            return raw
        try:
            resp = self.client.chat.completions.create(
                model="gpt-4o-mini",
                messages=[{"role": "user", "content": self.get_prompt(loop_id) + "\n" + raw}],
            )
            text = resp.choices[0].message.content.strip()
            self.cache[loop_id] = text
            return text
        except Exception:
            return raw

    def generate_end(self, choice: int) -> str:
        raw = GAME_END_1 if choice == 1 else GAME_END_2
        if not self.client:
            return raw
        try:
            resp = self.client.chat.completions.create(
                model="gpt-4o-mini",
                messages=[{"role": "user", "content": LLM_PROMPT_END + "\n" + raw}],
            )
            return resp.choices[0].message.content.strip()
        except Exception:
            return raw

